// Calculate all team statistics dynamically
export const computeTeamStats = () => {
  const stats = {};
  
  // Roster names normalized
  const allTeams = [
    "Century", "L'Flames", "Temple Boys", "D Fantastic 2", 
    "Orbyters", "y.fola", "Banters Fc", "Get Psyched", 
    "Gameverse", "Prime Time", "Dangerous Twins", "Footybands", "Titan"
  ];
  
  allTeams.forEach(team => {
    stats[team] = {
      played: 0,
      wins: 0,
      draws: 0,
      losses: 0,
      goalsScored: 0,
      goalsConceded: 0,
      points: 0, // group stage points only
      placement: ""
    };
  });

  // 1. Process Group Stage
  TOURNAMENT_MATCHES.groupStage.forEach(match => {
    const h = stats[match.home];
    const a = stats[match.away];
    if (!h || !a) return;
    
    h.played++;
    a.played++;
    h.goalsScored += match.homeScore;
    h.goalsConceded += match.awayScore;
    a.goalsScored += match.awayScore;
    a.goalsConceded += match.homeScore;
    
    if (match.homeScore > match.awayScore) {
      h.wins++;
      h.points += 3;
      a.losses++;
    } else if (match.homeScore < match.awayScore) {
      a.wins++;
      a.points += 3;
      h.losses++;
    } else {
      h.draws++;
      h.points += 1;
      a.draws++;
      a.points += 1;
    }
  });

  // 2. Process Knockouts
  TOURNAMENT_MATCHES.knockouts.forEach(match => {
    const h = stats[match.home];
    const a = stats[match.away];
    if (!h || !a) return;
    
    h.played++;
    a.played++;
    h.goalsScored += match.homeScore;
    h.goalsConceded += match.awayScore;
    a.goalsScored += match.awayScore;
    a.goalsConceded += match.homeScore;
    
    if (match.pensWinner) {
      h.draws++;
      a.draws++;
      if (match.pensWinner === match.home) {
        h.wins++;
        a.losses++;
      } else {
        a.wins++;
        h.losses++;
      }
    } else {
      if (match.homeScore > match.awayScore) {
        h.wins++;
        a.losses++;
      } else {
        a.wins++;
        h.losses++;
      }
    }
  });

  // Set final placements
  stats["Century"].placement = "Champion 🏆";
  stats["Orbyters"].placement = "Runner-Up 🥈";
  stats["y.fola"].placement = "3rd Place 🥉";
  stats["Gameverse"].placement = "4th Place";
  
  return stats;
};

// Seed attributes based on gamer tag weighted by tournament standings & points
export const generatePlayerAttributes = (gamerTag, teamName, placement, points, winRate) => {
  let hash = 0;
  const str = (gamerTag || "") + (teamName || "");
  for (let i = 0; i < str.length; i++) {
    hash = str.charCodeAt(i) + ((hash << 5) - hash);
  }
  const seed = Math.abs(hash);
  
  // Base offset based on tournament performance/placement
  let baseOffset = 60;
  let range = 25; // default range (60-85 overall)
  
  if (placement?.includes("Champion")) {
    baseOffset = 88;
    range = 10; // Champion: 88-98
  } else if (placement?.includes("Runner-Up")) {
    baseOffset = 82;
    range = 10; // Runner-Up: 82-92
  } else if (placement?.includes("3rd Place")) {
    baseOffset = 78;
    range = 10; // 3rd Place: 78-88
  } else if (placement?.includes("4th Place")) {
    baseOffset = 74;
    range = 10; // 4th Place: 74-84
  } else {
    // Scale standard teams based on group stage points and win rate
    const pts = points || 0;
    const wr = parseFloat(winRate || 0);
    baseOffset = 62 + Math.round(pts * 1.2) + Math.round(wr * 0.05);
    range = 12; // rating: baseOffset to baseOffset + 12 (e.g. 62-74 up to 75-87)
  }
  
  const attack = Math.min(99, baseOffset + (seed % range));
  const defense = Math.min(99, baseOffset - 2 + ((seed >> 2) % range));
  const passing = Math.min(99, baseOffset + 1 + ((seed >> 4) % range));
  const consistency = Math.min(99, baseOffset - 3 + ((seed >> 6) % range));
  const clutch = Math.min(99, baseOffset + 2 + ((seed >> 8) % range));
  
  return { attack, defense, passing, consistency, clutch };
};

